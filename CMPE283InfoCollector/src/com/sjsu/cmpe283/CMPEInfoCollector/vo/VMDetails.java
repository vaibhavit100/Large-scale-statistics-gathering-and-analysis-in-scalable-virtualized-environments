package com.sjsu.cmpe283.CMPEInfoCollector.vo;

/**
 * @author Bhargav
 *
 */
public class VMDetails {

	private String vmName;
	private String vmIP;
	private String vmURL;
	
	/**
	 * @return the vmName
	 */
	public String getVmName() {
		return vmName;
	}
	/**
	 * @param vmName the vmName to set
	 */
	public void setVmName(String vmName) {
		this.vmName = vmName;
	}
	/**
	 * @return the vmIP
	 */
	public String getVmIP() {
		return vmIP;
	}
	/**
	 * @param vmIP the vmIP to set
	 */
	public void setVmIP(String vmIP) {
		this.vmIP = vmIP;
	}
	/**
	 * @return the vmURL
	 */
	public String getVmURL() {
		return vmURL;
	}
	/**
	 * @param vmURL the vmURL to set
	 */
	public void setVmURL(String vmURL) {
		this.vmURL = vmURL;
	}
	
}
